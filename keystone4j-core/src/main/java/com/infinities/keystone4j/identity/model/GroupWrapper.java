package com.infinities.keystone4j.identity.model;


public class GroupWrapper {

	private Group group;


	public GroupWrapper() {

	}

	public GroupWrapper(Group group) {
		super();
		this.group = group;
	}

	public Group getGroup() {
		return group;
	}

	public void setGroup(Group group) {
		this.group = group;
	}

}
